<h3 class="mb-3 mt-5">¿Qué son las cookies?</h3>
<p>Las cookies son pequeños archivos de texto almacenados en su dispositivo cuando visita un sitio web. Registran información sobre su actividad, permitiendo que el sitio web recuerde sus preferencias y mejore su experiencia de navegación en futuras visitas.</p>
<p>Las cookies pueden almacenar datos como detalles de inicio de sesión, preferencias de idioma y configuraciones personalizadas. También ayudan a que el sitio web funcione correctamente y nos permiten analizar la interacción de los usuarios para mejorar nuestros servicios.</p>
<p>Las cookies pueden ser establecidas por el sitio web que visita (cookies de origen) o por servicios de terceros que utilizamos para análisis, publicidad o contenido adicional. Estas cookies de terceros pueden rastrear la actividad del usuario en diferentes sitios web para ofrecer publicidad personalizada y recopilar información sobre el comportamiento del usuario.</p>
<p>Para obtener más información sobre las cookies, visite www.aboutcookies.org o www.allaboutcookies.org.</p>

